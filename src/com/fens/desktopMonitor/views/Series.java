    private void initOtherComponentes(){
        
        //Centramos Form en Pantalla
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        this.setLocation(dim.width/2-this.getSize().width/2, dim.height/2-this.getSize().height/2);
    
        EmisorBO emisorBO = new EmisorBO(this.conn,idEmisor);
        this.emisorDto = emisorBO.getEmisor();
        if (this.emisorDto!=null){
            this.setTitle(this.getTitle() + " " + emisorDto.getRfc());
        }
        
        /*LLenamos tabla con series */    
        try{
            jTableSeries.setModel(series_action.fillSeries(idEmisor)); /*  Seteamos modelo a tabla */
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Error al cargar tabla de Series:\n" + e.toString(),"Error",JOptionPane.ERROR_MESSAGE); 
        }
        
        //Completamos control Combo de selección Estatus Serie
        comboEstatusSerie.addItem("Selecciona...");          
        comboEstatusSerie.addItem("ACTIVO");
        comboEstatusSerie.addItem("INACTIVO");
        
        //Completamos control Combo de selección Tipo Comprobante
        TipoComprobanteBO tipoComprobanteBO = new TipoComprobanteBO(null);
        Tipocomprobante[] tipoComprobantes = tipoComprobanteBO.findTipocomprobantes(-1, 0, 0, "");
        comboTipoComprobante.addItem("Selecciona...");
        for (Tipocomprobante item :  tipoComprobantes){
            comboTipoComprobante.addItem(item);
        }
        
    }
    
