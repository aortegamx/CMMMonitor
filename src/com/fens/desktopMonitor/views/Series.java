    private void cmdEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdEliminarActionPerformed

        int rowSelected;
        
        try{
             rowSelected = jTableSeries.getSelectedRow(); /* Obtenemos fila seleccionada*/            
        
        
            int idSerie = Integer.parseInt(txtIdSerie.getText()); /* Fila id */
            String nombreSerie = txtNombreSerie.getText(); /* Fila nombre */
            
             //Instanciamos conotrolador
            action = new series_action(this);
            
            int dialogResult = JOptionPane.showConfirmDialog (null, "¿Deseas eliminar la Serie '" + nombreSerie + "'?","Advertencia",JOptionPane.YES_NO_OPTION);
            if(dialogResult == JOptionPane.YES_OPTION){ 
                try {                

                    action.eliminarSerie(idSerie, idEmisor); /* Estatus = -1 -> Eliminado*/

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,"Ocurrio un error al eliminar el registro:\n " + ex.toString(),"Error",JOptionPane.ERROR_MESSAGE);
                }  
            
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "No ha seleccionado una Serie a eliminar en el listado.");
        }

    }//GEN-LAST:event_cmdEliminarActionPerformed

    private void jTableSeriesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSeriesMouseClicked
        if (evt.getClickCount() == 2) { /* Si da doble clic sobre tabla */
            
            int rowSelected = jTableSeries.getSelectedRow(); /* Obtenemos fila seleccionada*/
            
            int idSerie = Integer.parseInt(jTableSeries.getValueAt(rowSelected, 0).toString()); /* Fila id */
            
            /* Carga serie seleccionada */
            
            //Instanciamos controlador
            action = new series_action(this);
             
            try {
                action.cargaSerie(idSerie);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "No se encontraron datos del Emisor seleccionado. ");
            }
            
            
           
        }
    }//GEN-LAST:event_jTableSeriesMouseClicked

